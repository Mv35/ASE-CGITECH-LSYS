#ifndef TURTLEBRANCH_H_
#define TURTLEBRANCH_H_

#include <ngl/Vec4.h>
#include <ngl/Mat4.h>

///@file TurtleBranch.h
/// @brief Data structure used to store parameters to be push or popped when a branching symbol
/// is interpreted
///
struct TurtleBranch
{
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief index
  //----------------------------------------------------------------------------------------------------------------------
  int m_index;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief position
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec4 m_position;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief rotationMatrix
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Mat4 m_rotationMatrix;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief previousDirection
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec4 m_previousDirection;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief drawStep
  //----------------------------------------------------------------------------------------------------------------------
  float m_drawStep;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief pRadius
  //----------------------------------------------------------------------------------------------------------------------
  float m_pRadius;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief colours
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec4 m_colour;
};

#endif // TURTLEBRANCH_H_
