#ifndef TURTLEPARAMETERS_H_
#define TURTLEPARAMETERS_H_

///@file TurtleParameter.h
/// @brief parameter data structure used by the Turtle class in order to interpret the passed string according
/// to the passed dictionary/alhpabet

struct TurtleParameters
{
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief angle
    //----------------------------------------------------------------------------------------------------------------------
    float m_angle;
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief iteration
    //----------------------------------------------------------------------------------------------------------------------
    int m_iteration;
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief drawLenght
    //----------------------------------------------------------------------------------------------------------------------
    float m_drawLenght;
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief startX
    //----------------------------------------------------------------------------------------------------------------------
    float m_startX = 0.0f;
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief startY
    //----------------------------------------------------------------------------------------------------------------------
    float m_startY =0.0f;
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief startZ
    //----------------------------------------------------------------------------------------------------------------------
    float m_startZ =0.0f;
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief dictSymbols
    //----------------------------------------------------------------------------------------------------------------------
    std::vector<std::string> m_dictSymbols;
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief constants
    //----------------------------------------------------------------------------------------------------------------------
    std::vector<std::string> m_constants;
    //----------------------------------------------------------------------------------------------------------------------
    ///@brief lSys
    //----------------------------------------------------------------------------------------------------------------------
    std::vector<std::string> m_lSys;
};
#endif // TURTLEPARAMETERS_H_
