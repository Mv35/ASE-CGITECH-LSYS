#ifndef TURTLENODES_H_
#define TURTLENODES_H_

#include <ngl/Vec4.h>

///@file TurtleNodes.h
/// @brief Data Structure to hold data of the nodes in order to generate a representable mesh
///
struct TurtleNodes
{

  //----------------------------------------------------------------------------------------------------------------------
  ///@brief position
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec4 m_position;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief previousDirection
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec4 m_previousDirection;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief drawStep
  //----------------------------------------------------------------------------------------------------------------------
  float m_drawStep;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief pRadius
  //----------------------------------------------------------------------------------------------------------------------
  float m_pRadius;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief colour
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec4 m_colour;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief closingBracket
  //----------------------------------------------------------------------------------------------------------------------
  bool m_closingBracket;
};

#endif // TURTLENODES_H_
