#ifndef TREEDATA_H
#define TREEDATA_H

#include <vector>
#include <ngl/Vec4.h>

///@file TreeData.h
/// @brief A data structure used to store geometric information (VAO)
///
struct TreeData
{
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief drawtype
  //----------------------------------------------------------------------------------------------------------------------
  int drawType=1;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief positions
  //----------------------------------------------------------------------------------------------------------------------
  std::vector<ngl::Vec4> m_positions;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief points
  //----------------------------------------------------------------------------------------------------------------------
  std::vector<ngl::Vec4> m_points;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief normals
  //----------------------------------------------------------------------------------------------------------------------
  std::vector<ngl::Vec4> m_normals;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief lineIndices
  //----------------------------------------------------------------------------------------------------------------------
  std::vector<int> m_lineIndices;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief indices
  //----------------------------------------------------------------------------------------------------------------------
  std::vector<int> m_indices;
  //----------------------------------------------------------------------------------------------------------------------
  ///@brief colours
  //----------------------------------------------------------------------------------------------------------------------
  std::vector<ngl::Vec4> m_colours;
};
#endif // TREEDATA_H
